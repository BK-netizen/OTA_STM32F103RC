//指针初始化
void U0Rx_PtrInit(void)
{
	U0CB.URxDataIN = &U0CB.URxDataPtr[0];           //IN指针指向SE指针对结构体数组的0号成员
	U0CB.URxDataOUT = &U0CB.URxDataPtr[0];	        //OUT指针指向SE指针对结构体数组的0号成员
	U0CB.URxDataEND = &U0CB.URxDataPtr[NUM-1];      //END指针指向SE指针对结构体数组的最后一个成员
	U0CB.URxDataIN->start = U0_RxBuff;              //使用IN指针指向的SE指针对中的S指针标记第一次接收的起始位置
	U0CB.URxCounter = 0;                            //累计接收数量清零
}

void u0_printf(char *format, ...)
{	
    uint16_t i;
    va_list listdata;
    
    va_start(listdata, format);
    vsprintf((char *)U0_TxBuff, format, listdata);  // 格式化到缓冲区
    va_end(listdata);

    uint16_t len = strlen((const char*)U0_TxBuff);  // 提前计算长度
    
    for(i = 0; i < len; i++) 
	{
        while(__HAL_UART_GET_FLAG(&huart1, UART_FLAG_TXE) == RESET);  // 等待发送缓冲区空
        HAL_UART_Transmit(&huart1, (uint8_t*)&U0_TxBuff[i], 1, HAL_MAX_DELAY);  // 发送单个字符
    }
    
    while(__HAL_UART_GET_FLAG(&huart1, UART_FLAG_TC) == RESET);  // 等待传输完成
}
